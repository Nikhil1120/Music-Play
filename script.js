const mainDiv = document.getElementById('main');
const searchInput = document.getElementById('searchInput');
const searchButton = document.getElementById('searchButton');

// Create exit button
const exitButton = document.createElement('button');
exitButton.textContent = 'Exit';
exitButton.style.display = 'none';
exitButton.style.marginTop='50px';
exitButton.style.backgroundColor='blue';
exitButton.style.color='white';
exitButton.style.fontSize='15px';

mainDiv.parentNode.appendChild(exitButton);

// Function to display music data
function displayData(data) {
    mainDiv.innerHTML = '';
    data.map((ele) => {
        const musicDiv = document.createElement('div');
        musicDiv.classList.add('music');
        musicDiv.dataset.title = ele.title;

        const image = document.createElement('img');
        image.classList.add('img-photo');
        image.src = ele.imageLink;
        image.alt = ele.title;

        const title = document.createElement('h2');
        title.textContent = ele.title;

        const year = document.createElement('h4');
        year.textContent = ele.year;

        const artistName = document.createElement('p');
        artistName.textContent = ele.artist;

        const link = document.createElement('a');
        link.textContent = "Favorite Song";
        link.href = ele.link;
        link.target = "_blank";

        musicDiv.append(image, title, artistName, year, link);
        mainDiv.appendChild(musicDiv);
    });
}
// Initial display
displayData(music);


// Function to search music data
function searchMusic(searchTerm) {
    const filteredData = music.filter((ele) =>
        ele.title.toLowerCase().includes(searchTerm.toLowerCase())
    );
    displayData(filteredData);
    exitButton.style.display = 'block';
    searchInput.value = searchTerm;
}
// Exit button event listener
exitButton.addEventListener('click', resetSearch);

// Function to reset search
function resetSearch() {
    searchInput.value = '';
    displayData(music);
    exitButton.style.display = 'none';
}



// Search event listener
searchButton.addEventListener('click', () => {
    const searchTerm = searchInput.value.trim();
    if (searchTerm) {
        searchMusic(searchTerm);
    } else {
        resetSearch();
    }
});


